export function getWorkspaceSceneDigestPayload(
  scene: WorkspaceSceneSnapshot
): WorkspaceSceneDigestPayload {
  const parsed = WorkspaceSceneSnapshotSchema.parse(scene);
  return {
    schemaVersion: parsed.schemaVersion,
    catalog: parsed.catalog,
    navigation: parsed.navigation,
    editors: parsed.editors,
    agents: parsed.agents,
    terminals: parsed.terminals,
    todos: parsed.todos,
    selections: parsed.selections,
    resources: parsed.resources,
  };
}

export function canonicalizeWorkspaceScene(scene: WorkspaceSceneSnapshot): string {
  return canonicalJson(getWorkspaceSceneDigestPayload(scene));
}

export async function digestWorkspaceScene(scene: WorkspaceSceneSnapshot): Promise<string> {
  const bytes = new TextEncoder().encode(canonicalizeWorkspaceScene(scene));
  const digest = await globalThis.crypto.subtle.digest('SHA-256', bytes);
  return Array.from(new Uint8Array(digest), (byte) => byte.toString(16).padStart(2, '0')).join('');
}
