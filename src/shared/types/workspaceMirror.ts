function boundedRecord<Value extends z.ZodType>(valueSchema: Value) {
  return z.record(boundedId, valueSchema).superRefine((value, context) => {
    if (Object.keys(value).length > MAX_COLLECTION_SIZE) {
      context.addIssue({
        code: 'too_big',
        maximum: MAX_COLLECTION_SIZE,
        origin: 'object',
        inclusive: true,
        message: `Record cannot contain more than ${MAX_COLLECTION_SIZE} entries`,
      });
    }
  });
}

