export const StateReplayFrameSchema = z
  .strictObject({
    t: z.literal('state.replay'),
    hostEpoch: WorkspaceHostEpochSchema,
    sceneId: WorkspaceSceneIdSchema,
    fromRevision: positiveSafeInteger,
    toRevision: positiveSafeInteger,
    events: z.array(WorkspaceSceneEventSchema).max(MAX_COLLECTION_SIZE),
  })
  .superRefine((value, context) => {
    if (value.toRevision < value.fromRevision) {
      context.addIssue({
        code: 'custom',
        path: ['toRevision'],
        message: 'toRevision cannot be less than fromRevision',
      });
    }
    if (value.events.length !== value.toRevision - value.fromRevision + 1) {
      context.addIssue({
        code: 'custom',
        path: ['events'],
        message: 'Replay must contain one event for each revision in its range',
      });
    }
    for (const [index, event] of value.events.entries()) {
      if (event.revision !== value.fromRevision + index) {
        context.addIssue({
          code: 'custom',
          path: ['events', index, 'revision'],
          message: 'Replay event revisions must be contiguous',
        });
      }
      if (event.hostEpoch !== value.hostEpoch || event.sceneId !== value.sceneId) {
        context.addIssue({
          code: 'custom',
          path: ['events', index],
          message: 'Replay events must belong to the replay scene and host epoch',
        });
      }
    }
  });

