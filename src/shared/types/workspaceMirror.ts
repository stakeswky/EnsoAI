function isBoundedJsonValue(value: unknown, depth = 0, entryBudget = { value: 0 }): boolean {
  if (depth > MAX_JSON_DEPTH) {
    return false;
  }
  if (
    value === null ||
    typeof value === 'boolean' ||
    typeof value === 'string' ||
    (typeof value === 'number' && Number.isFinite(value))
  ) {
    return typeof value !== 'string' || value.length <= MAX_TEXT_LENGTH;
  }
  if (Array.isArray(value)) {
    if (value.length > MAX_JSON_ENTRIES) {
      return false;
    }
    entryBudget.value += value.length;
    return (
      entryBudget.value <= MAX_JSON_ENTRIES &&
      value.every((item) => isBoundedJsonValue(item, depth + 1, entryBudget))
    );
  }
  if (typeof value !== 'object') {
    return false;
  }
  const prototype = Object.getPrototypeOf(value);
  if (prototype !== Object.prototype && prototype !== null) {
    return false;
  }
  const entries = Object.entries(value);
  entryBudget.value += entries.length;
  return (
    entries.length <= MAX_JSON_ENTRIES &&
    entryBudget.value <= MAX_JSON_ENTRIES &&
    entries.every(
      ([key, item]) =>
        key.length <= MAX_NAME_LENGTH && isBoundedJsonValue(item, depth + 1, entryBudget)
    )
  );
}

export const JsonValueSchema = z.custom<JsonValue>(
  (value) => isBoundedJsonValue(value),
  'Expected a bounded JSON value'
);

