export const ResourceUploadBeginFrameSchema = z
  .strictObject({
    t: z.literal('resource.upload.begin'),
    requestId: boundedId,
    uploadId: boundedId,
    displayName: z.string().min(1).max(512),
    mime: z.string().min(1).max(256).optional(),
    totalBytes: safeInteger,
    totalChunks: positiveSafeInteger,
    checksum: sha256,
  })
  .superRefine((value, context) => {
    if (value.totalBytes > WORKSPACE_MIRROR_MAX_RESOURCE_BYTES) {
      context.addIssue({
        code: 'too_big',
        maximum: WORKSPACE_MIRROR_MAX_RESOURCE_BYTES,
        origin: 'number',
        inclusive: true,
        path: ['totalBytes'],
        message: 'Resource upload exceeds the size limit',
      });
    }
    const expectedChunks = Math.max(
      1,
      Math.ceil(value.totalBytes / WORKSPACE_MIRROR_MAX_RESOURCE_CHUNK_BYTES)
    );
    if (value.totalChunks !== expectedChunks) {
      context.addIssue({
        code: 'custom',
        path: ['totalChunks'],
        message: 'Resource upload chunk count does not match total bytes',
      });
    }
  });

