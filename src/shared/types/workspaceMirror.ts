function canonicalizeJsonValue(value: unknown, ancestors: Set<object>): string {
  if (value === null || typeof value === 'boolean' || typeof value === 'string') {
    return JSON.stringify(value);
  }
  if (typeof value === 'number') {
    if (!Number.isFinite(value)) {
      throw new TypeError('Canonical JSON cannot contain a non-finite number');
    }
    return JSON.stringify(value);
  }
  if (Array.isArray(value)) {
    if (ancestors.has(value)) {
      throw new TypeError('Canonical JSON cannot contain a cycle');
    }
    ancestors.add(value);
    const result = `[${value.map((item) => canonicalizeJsonValue(item, ancestors)).join(',')}]`;
    ancestors.delete(value);
    return result;
  }
  if (typeof value !== 'object') {
    throw new TypeError(`Canonical JSON cannot contain ${typeof value}`);
  }
  const prototype = Object.getPrototypeOf(value);
  if (prototype !== Object.prototype && prototype !== null) {
    throw new TypeError('Canonical JSON accepts only plain objects');
  }
  if (ancestors.has(value)) {
    throw new TypeError('Canonical JSON cannot contain a cycle');
  }
  ancestors.add(value);
  const record = value as Record<string, unknown>;
  const result = `{${Object.keys(record)
    .sort()
    .map((key) => `${JSON.stringify(key)}:${canonicalizeJsonValue(record[key], ancestors)}`)
    .join(',')}}`;
  ancestors.delete(value);
  return result;
}

export function canonicalJson(value: unknown): string {
  return canonicalizeJsonValue(value, new Set());
}

