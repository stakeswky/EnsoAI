/** Opaque URI returned to remote renderers for host-owned attachments. */
export const WORKSPACE_RESOURCE_URI_PREFIX = 'enso-resource://';

/**
 * Resource ids are generated by the host and deliberately use a URL-safe
 * alphabet. Keeping the validation here lets renderer URL conversion and the
 * main-process protocol handler agree without ever treating an opaque id as
 * a filesystem path.
 */
export function isWorkspaceResourceId(value: string): boolean {
  return /^[A-Za-z0-9._:-]{1,256}$/.test(value);
}

export function workspaceResourceIdFromUri(value: string): string | null {
  if (!value.toLowerCase().startsWith(WORKSPACE_RESOURCE_URI_PREFIX)) return null;
  const resourceId = value.slice(WORKSPACE_RESOURCE_URI_PREFIX.length);
  return isWorkspaceResourceId(resourceId) ? resourceId : null;
}

